class Solution{
public:
	int countZeros(vector<vector<int>>A)
	{
		//code here
		int N=A.size(),M=A[0].size(),r=0,c=M-1,ans=0;
	    
	    while(r<N && c>=0){
	        if(A[r][c]==0){
	            ans+=c+1;
	            r++;
	        }
	        else if(A[r][c]==1)c--;
	    }
	    return ans;
	}
};

