class Solution {
  public:
    void printSquare(int n) {
        // code here
        for(int r=1;r<2*n;r++){
            for(int c=1;c<2*n;c++){
                cout<<max(abs(n-r)+1,abs(n-c)+1)<<" ";
            }
            cout<<endl;
        }
    }
};

//{ Driver Code Starts.

int main() {
    int t;
    cin >> t;
    while (t--) {
        int n;
        cin >> n;

        Solution ob;
        ob.printSquare(n);
    }
    return 0;
}
