class Solution {
  public:
    long long reversedBits(long long X) {
        // code here
        long long num=0,count=0;
        
        while(count<32){
            long long rem=X&1;
            num=num*2+rem;
            
            X=X>>1;
            count++;
        }
        return num;
    }
};

