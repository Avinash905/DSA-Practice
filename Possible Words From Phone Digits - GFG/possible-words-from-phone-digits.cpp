class Solution
{
    public:
    void solve(int arr[],int idx,string op,int N,unordered_map<int,string> mp,vector<string>& ans){
        if(idx>=N){
            ans.push_back(op);
            return;
        }
        string chars=mp[arr[idx]];
        for(int i=0;i<chars.size();i++){
            string op1=op;
            op1+=chars[i];
            solve(arr,idx+1,op1,N,mp,ans);
        }
    }
    //Function to find list of all words possible by pressing given numbers.
    vector<string> possibleWords(int a[], int N)
    {
        //Your code here
        vector<string> ans;
        unordered_map<int,string> mp;
        mp[2]="abc";
        mp[3]="def";
        mp[4]="ghi";
        mp[5]="jkl";
        mp[6]="mno";
        mp[7]="pqrs";
        mp[8]="tuv";
        mp[9]="wxyz";
        
        solve(a,0,"",N,mp,ans);
        return ans;
    }
};


