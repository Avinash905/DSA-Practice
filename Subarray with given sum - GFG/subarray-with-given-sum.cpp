class Solution
{
    public:
    //Function to find a continuous sub-array which adds up to a given number.
    vector<int> subarraySum(vector<int>arr, int n, long long s)
    {
        // Your code here
        long long sum=arr[0],left=0,right=0;
        while(right<n){
            // cout<<sum<<endl;
            if(sum==s)return {left+1,right+1};
            else if(sum<s){
                right++;
                sum+=arr[right];
            }
            else{
                sum-=arr[left];
                left++;
                if(right<=left){
                    right=left;
                    sum=arr[left];
                }
            }
        }
        return {-1};
    }
};

