class Solution
{
	public:
		vector<int> find_sum(int n)
		{
		    // Code here
            int i=1,evenSum=0,oddSum=0;
            while(i<=n){
                if(i%2==0){
                    evenSum+=i;
                }
                else oddSum+=i;
                i++;
            }
            return {oddSum,evenSum};
		}
};

